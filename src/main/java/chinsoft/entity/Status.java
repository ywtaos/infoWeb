package chinsoft.entity;

public class Status {
	
		private long count;
		private String countK;
		private boolean is;
					
		public long getCount() {
			return count;
		}
		public void setCount(long count) {
			this.count = count;
		}
		public String getCountK() {
			return countK;
		}
		public void setCountK(String countK) {
			this.countK = countK;
		}
		public boolean isIs() {
			return is;
		}
		public void setIs(boolean is) {
			this.is = is;
		}
		
		
}
